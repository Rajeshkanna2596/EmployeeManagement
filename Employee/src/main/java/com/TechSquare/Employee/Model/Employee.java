package com.TechSquare.Employee.Model;

public class Employee {
	private String EmployeeName;
	private int EmployeeId;
	private String EmployeeAddress;
	private Long EmployeeContectNumber;
	public String getEmployeeName() {
		return EmployeeName;
	}
	public void setEmployeeName(String employeeName) {
		EmployeeName = employeeName;
	}
	public int getEmployeeId() {
		return EmployeeId;
	}
	public void setEmployeeId(int employeeId) {
		EmployeeId = employeeId;
	}
	public String getEmployeeAddress() {
		return EmployeeAddress;
	}
	public void setEmployeeAddress(String employeeAddress) {
		EmployeeAddress = employeeAddress;
	}
	public Long getEmployeeContectNumber() {
		return EmployeeContectNumber;
	}
	public void setEmployeeContectNumber(Long employeeContectNumber) {
		EmployeeContectNumber = employeeContectNumber;
	}
	

}
